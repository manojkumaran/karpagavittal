        <p class="desc"> 
                With the benign blessings of the Kanchi Seer, Maha Periyaval, Sri Chandrasekarendra Saraswathi Swamigal, two Sannidhies, one for Sri Karpaga Vinayagar and the other for Sri Anjali Anjaneyar were built at Santhi Nagar, Adambakkam, Chennai and consecrated during 1995. The very next year saw another sannidhi for Lord Murugan. Structures for Durgai Amman, Dakshinamurthy and Navagrahams were raised in 2001. 
                A beautiful sannidhi for Sri Rukmini sametha Sri Pandurangan, reminiscing the Pandaripuram temple, was later built and it's Kumbabhishekam was conducted in 2004 and after that entire temple was popularly called as ‘Sri Karpaga Vinayagar Sri Pandurangan temple’. 
                Renovation and kumbabhishekam of Murugan sannidhi took place in 2013. 
